	<h2>1. Périodes d’ouverture</h2>
	<p><strong>SAISON:</strong><br>Du 01.05 - 30.09 de 08h00 - 18h00 (ou 19h00 défini le jour même)<br>(sauf manifestation particulière)</p>
	<p><strong>HORS-SAISON:</strong><br>Du 08.01 - 30.04 et du 01.10 au 15.12 de 8h00 - 16h00<br>(sauf manifestation particulière)<br>Sous réserve d’emplacements disponibles dépendant des conditions météorologiques ou de la privatisation du site.</p>
	<p><strong>PRIVATISATION DES ETANGS :</strong><br> Il est possible de privatiser tout ou partie des étangs, voir tarifs ci-dessous. Mêmes conditions pour les concours de pêche.</p>
	<h2>2. Acceptation du règlement</h2>
	<p>L’autorisation de pêche sur les étangs « FESCHWEIER 2 & 3 » ainsi que «BAGGERWEIER – SCHWAMMWEIER» est soumise à la connaissance et à l’acceptation du règlement interne de pêche ainsi que des dispositions légales en vigueur. La possession d’une carte de pêche valide pour l’étang concerné est obligatoire.</p>
	<h2>3. Carte de pêche</h2>
	<p>La carte de pêche est strictement personnelle, elle ne pourra être ni prêtée ni vendue sous peine de s’exposer au risque de faux et usage de faux. Elle devra être présentée à toutes requêtes à un collaborateur de « Erliefnis Baggerweier a.s.b.l. » qui en fera la demande.</p>
	<h2>4. Nos Tarifs:</strong></h2>
	 <p>Voir grille tarifaire ci-dessous</p>
    <h2>Règles générales de pêche</h2>
    <p>
    5. Tout poisson pris doit être immédiatement remis à l’eau dans les meilleures conditions possibles. L’utilisation d’un tapis de réception est obligatoire.<br>
    6. Les bourriches et sac de conservation sont interdits (ex. Concours de pêche avec max 25 kg par bourriche)<br>
    7. La pêche se fait avec une seule canne au coup (max 11,5 mètres)<br>
    8. Franglaise autorisé (max 7 mètres), canne “à la chinoise” défendue<br>
    9. La pêche se fait avec un hameçon simple et sans ardillon<br>
    10. Les tresses sont interdites.<br>
    11. Pour le respect du poisson, un tapis de réception et une grande épuisette sont obligatoires<br>
    12. La pêche au pain est strictement défendue, aussi bien comme amorce que comme appât.<br>
    13. Sont autorisés : Pellets, Grains cuits (Maïs, chènevis, pois chiche, blé), asticots, gozzers, vers de terre, pâte.<br>
    14. La pêche est autorisée uniquement aux emplacements indiqués.
    </p>

	<h2>15. Techniques</h2>
	<p>La pêche au moulinet (feeder) est autorisée pendant la saison uniquement aux postes indiqués (pontons 20 à 30). Pêche Carpodrome : feeder, méthode feeder, waggler, pellet waggler. La pêche aux carnassiers ou aux leurres est autorisée uniquement au « Baggerweier – Schwammweier ».</p>
	<p>Hors saison, la pêche au feeder est autorisée à tous les postes du Carpodrome.</p>

	<h2>16. Interdictions</h2>
	<p>Les activités suivantes sont interdites sur les zones de pêche :</p>
	<ul>
		<li>La baignade</li>
		<li>Entrer dans l’eau pour pêcher ou amorcer, ou introduire tout élément de réservation dans l’eau ou sur les berges</li>
		<li>L’utilisation de toute embarcation</li>
		<li>Le campement de nuit</li>
		<li>Faire du feu en dehors des zones prévues pour BBQ</li>
		<li>Enlever, endommager ou détruire les plantes ou arbres</li>
		<li>Déposer des déchets dans l’étang ou aux alentours (des poubelles sont à disposition)</li>
		<li>Marcher sur la glace en cas de gel, se promener ou patiner sur les plans d’eau gelés ou percer la glace.</li>
	</ul>
	<p>Les visiteurs sont invités à respecter la propreté des lieux !</p>

	<h2>17. Confort</h2>
	<ul>
		<li>Les accompagnateurs doivent respecter la tranquillité des pêcheurs</li>
		<li>Les cannes de pêche ne pourront pas être laissées à l’eau sans surveillance constante du pêcheur.</li>
	</ul>

	<h2>18. Concours</h2>
	<p>Le règlement interne de pêche peut être modifié en cas de concours de pêche organisé par Erliefnis Baggerweier a.s.b.l.</p>

	<h2>19. Règles spécifiques à la zone Baggerweier-Schwammweier</h2>
	<p>Seule la zone délimitée en jaune est autorisée à la pêche du 1er octobre au 30 avril, dans le respect de la nature.</p>
	<ul>
		<li>Les poisson pris doivent être remis à l’eau.</li>
		<li><strong>Saison : du 01.05 au 15.09 (sauf manifestations)</strong></li>
		<li>Autorisée : Pêche au coup (sans moulinet) sur les postes indiqués.</li>
		<li>Interdite : Toute pêche au moulinet. Toute pêche sur d’autres postes.</li>
	</ul>
	<ul>
		<li><strong>Hors – Saison du 08.01 au 31.04 et du 16.09 au 15.12 (sauf manifestations)</strong></li>
		<li>Autorisée : Pêche au coup (sans moulinet) et la pêche au moulinet sur les postes indiqués.</li>
	</ul>
