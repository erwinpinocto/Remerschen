<p>Carpodrome ponds &amp; pond F1, as well as fishing on marked spots at Baggerweier – Schwammweier.<br>
Fishing is authorised from 8 January to 15 December.</p>

<h2>1. Opening periods</h2>
<p><strong>SEASON:</strong><br>
From 01.05 to 30.09 from 08:00 to 18:00 (or 19:00 if defined on the same day)<br>
(except for special events)</p>

<p><strong>OFF-SEASON:</strong><br>
From 08.01 to 30.04 and from 01.10 to 15.12 from 08:00 to 16:00<br>
(except for special events)<br>
Subject to available spots depending on weather conditions or site privatisation.</p>

<p><strong>PRIVATISATION OF THE PONDS:</strong><br>
It is possible to privatise all or part of the ponds; see prices below. The same conditions apply to fishing competitions.</p>

<h2>2. Acceptance of the regulations</h2>
<p>Authorisation to fish on the ponds “FESCHWEIER 2 &amp; 3” as well as “BAGGERWEIER – SCHWAMMWEIER” is subject to knowledge and acceptance of the internal fishing regulations as well as the applicable legal provisions. Possession of a valid fishing permit for the pond concerned is mandatory.</p>

<h2>3. Fishing permit</h2>
<p>The fishing permit is strictly personal and may not be lent or sold, otherwise the holder risks prosecution for forgery and/or use of forged documents. It must be shown on request to a collaborator of “Erliefnis Baggerweier a.s.b.l.” who asks for it.</p>

<h2>4. Our prices:</h2>
<p> Please refer to the price list below.</p>

<h2>General fishing rules</h2>
<p>
5. Any fish caught must be immediately returned to the water in the best possible conditions. The use of an unhooking mat is mandatory.<br>
6. Keepnets and keep bags are prohibited (e.g. fishing competitions with a maximum of 25 kg per keepnet).<br>
7. Fishing is allowed with only one pole rod (max. 11.5 metres).<br>
8. Franglaise" is allowed (max. 7 metres); “Chinese-style” rods are forbidden.<br>
9. Fishing must be done with a single barbless hook.<br>
10. Braided line is forbidden.<br>
11. For the protection of the fish, an unhooking mat and a large landing net are mandatory.<br>
12. Fishing with bread is strictly forbidden, سواء as bait or as groundbait.<br>
13. Authorised bait and feed: pellets, cooked grains (maize, hemp seed, chickpeas, wheat), maggots, gozzers, earthworms, dough.<br>
14. Fishing is authorised only at the designated spots.
</p>

<h2>15. Techniques</h2>
<p>Reel fishing (feeder) is authorised during the season only at the designated spots (pontoons 20 to 30). Carp fishing techniques: feeder, method feeder, waggler, pellet waggler. Predatory fish fishing or lure fishing is authorised only at “Baggerweier – Schwammweier”.</p>
<p>Outside the season, feeder fishing is authorised at all spots of the Carpodrome.</p>

<h2>16. Prohibitions</h2>
<p>The following activities are prohibited in the fishing zone </p>
<ul>
    <li>Swimming.</li>
    <li>Entering the water to fish or bait, or placing any object in the water or on the banks to reserve a spot.</li>
    <li>Use of any boat.</li>
    <li>Night camping.</li>
    <li>Lighting fires outside the designated BBQ areas.</li>
    <li>Removing, damaging or destroying plants or trees.</li>
    <li>Depositing waste in the pond or around it (bins are available).</li>
    <li>Walking on ice in case of frost, strolling or skating on frozen water bodies, or drilling into the ice.</li>
</ul>
<p>Visitors are kindly asked to keep the site clean.</p>

<h2>17. Comfort</h2>
<ul>
    <li>Accompanying persons must respect the tranquillity of anglers.</li>
    <li>Fishing rods may not be left in the water without the angler’s constant supervision.</li>
</ul>

<h2>18. Competitions</h2>
<p>The internal fishing regulations may be modified in the event of a fishing competition organised by Erliefnis Baggerweier a.s.b.l.</p>

<h2>19. Specific rules for the Baggerweier–Schwammweier area</h2>
<p>Only the area marked in yellow is authorised for fishing from 1 October to 30 April, in respect of nature.</p>
<ul>
    <li>Fish caught must be returned to the water.</li>
    <li><strong>Season: from 01.05 to 15.09 (except events)</strong></li>
    <li>Authorised: pole fishing (without reel) at the designated spots.</li>
    <li>Prohibited: all reel fishing. Fishing at any other spots.</li>
</ul>
<ul>
    <li><strong>Off-season: from 08.01 to 31.03 and from 16.09 to 15.12 (except events)</strong></li>
    <li>Authorised: pole fishing (without reel) and reel fishing at the designated spots.</li>
</ul>

