<p>Carpodrome-Teiche und Teich F1 sowie das Angeln an gekennzeichneten Stellen am BAGGERWEIER.<br>
Das Angeln ist vom 8. Januar bis zum 15. Dezember erlaubt.</p>

<h2>1. Öffnungszeiten</h2>
<p><strong>SAISON:</strong><br>
Vom 01.05. bis 30.09. von 08:00 bis 18:00 Uhr (oder 19:00 Uhr, je nach Tagesentscheidung)<br>
(außer bei besonderen Veranstaltungen)</p>

<p><strong>AUSSERHALB DER SAISON:</strong><br>
Vom 08.01. bis 30.04. und vom 01.10. bis 15.12. von 08:00 bis 16:00 Uhr<br>
(außer bei besonderen Veranstaltungen)<br>
Vorbehaltlich verfügbarer Plätze, abhängig von den Wetterbedingungen oder einer Privatisierung der Anlage.</p>

<p><strong>PRIVATISIERUNG DER TEICHE:</strong><br>
Es ist möglich, die Teiche ganz oder teilweise zu privatisieren; siehe Tarifliste unten. Dieselben Bedingungen gelten für Angelwettbewerbe.</p>

<h2>2. Anerkennung des Reglements</h2>
<p>Die Erlaubnis zum Angeln in den Teichen „FESCHWEIER 2 &amp; 3“ sowie „BAGGERWEIER – SCHWAMMWEIER“ setzt die Kenntnis und Anerkennung der internen Angelordnung sowie der geltenden gesetzlichen Bestimmungen voraus. Der Besitz einer gültigen Angelkarte für den betreffenden Teich ist obligatorisch.</p>

<h2>3. Angelkarte</h2>
<p>Die Angelkarte ist streng persönlich. Sie darf weder verliehen noch verkauft werden, da andernfalls das Risiko von Urkundenfälschung und Gebrauch gefälschter Dokumente besteht. Sie ist auf Verlangen einem Mitarbeiter von „Erliefnis Baggerweier a.s.b.l.“ vorzuzeigen.</p>

<h2>4. Unsere Tarife:</h2>
<p>Siehe Tarifliste unten</p>

<h2>Allgemeine Angelregeln</h2>
<p>
5. Jeder gefangene Fisch muss sofort unter bestmöglichen Bedingungen wieder ins Wasser zurückgesetzt werden. Die Verwendung einer Abhakmatte ist verpflichtend.<br>
6. Setzkescher und Aufbewahrungssäcke sind verboten (z. B. bei Angelwettbewerben mit max. 25 kg pro Setzkescher).<br>
7. Es darf nur mit einer einzigen Stipprute geangelt werden (max. 11,5 Meter).<br>
8. Franglais ist erlaubt (max. 7 Meter), eine Rute „à la chinoise“ ist verboten.<br>
9. Es darf nur mit einem Einfachhaken und ohne Widerhaken geangelt werden.<br>
10. Geflochtene Schnüre sind verboten.<br>
11. Zum Schutz der Fische sind eine Abhakmatte und ein großer Kescher verpflichtend.<br>
12. Das Angeln mit Brot ist strikt verboten, sowohl als Anfütterung als auch als Köder.<br>
13. Erlaubt sind: Pellets, gekochte Körner (Mais, Hanf, Kichererbsen, Weizen), Maden, Gozzers, Regenwürmer, Teig.<br>
14. Das Angeln ist nur an den gekennzeichneten Plätzen erlaubt.
</p>

<h2>15. Techniken</h2>
<p>Das Angeln mit der Rolle (Feeder) ist während der Saison nur an den gekennzeichneten Plätzen erlaubt (Ponton 20 bis 30). Erlaubte Carpodrome-Techniken: Feeder, Method Feeder, Waggler, Pellet Waggler. Das Angeln auf Raubfische oder mit Kunstködern ist ausschließlich am „Baggerweier – Schwammweier“ erlaubt.</p>
<p>Außerhalb der Saison ist das Feederangeln an allen Plätzen des Carpodromes erlaubt.</p>

<h2>16. Verbote</h2>
<p>Die folgenden Aktivitäten sind in den Angelzonen verboten:</p>
<ul>
    <li>Baden.</li>
    <li>Das Betreten des Wassers zum Angeln oder Anfüttern sowie das Einbringen jeglicher Gegenstände zur Platzreservierung ins Wasser oder an die Ufer.</li>
    <li>Die Benutzung jeglicher Boote.</li>
    <li>Nachtcamping.</li>
    <li>Feuer machen außerhalb der dafür vorgesehenen BBQ-Bereiche.</li>
    <li>Das Entfernen, Beschädigen oder Zerstören von Pflanzen oder Bäumen.</li>
    <li>Das Entsorgen von Abfällen im Teich oder in dessen Umgebung (Mülleimer sind vorhanden).</li>
    <li>Das Betreten des Eises bei Frost, das Spazierengehen oder Schlittschuhlaufen auf zugefrorenen Wasserflächen sowie das Bohren von Löchern ins Eis.</li>
</ul>
<p>Die Besucher werden gebeten, die Sauberkeit der Anlage zu respektieren!</p>

<h2>17. Komfort</h2>
<ul>
    <li>Begleitpersonen müssen die Ruhe der Angler respektieren.</li>
    <li>Die Angelruten dürfen nicht ohne ständige Aufsicht des Anglers im Wasser gelassen werden.</li>
</ul>

<h2>18. Wettbewerbe</h2>
<p>Das interne Angelreglement kann bei einem von Erliefnis Baggerweier a.s.b.l. organisierten Angelwettbewerb angepasst werden.</p>

<h2>19. Besondere Regeln für den Bereich Baggerweier–Schwammweier</h2>
<p>Nur der gelb markierte Bereich ist vom 1. Oktober bis 30. April zum Angeln freigegeben, unter Berücksichtigung der Natur.</p>
<ul>
    <li>Gefangene Fische müssen wieder ins Wasser zurückgesetzt werden.</li>
    <li><strong>Saison: vom 01.05. bis 15.09. (außer bei Veranstaltungen)</strong></li>
    <li>Erlaubt: Stippangeln (ohne Rolle) an den gekennzeichneten Plätzen.</li>
    <li>Verboten: Jegliches Angeln mit Rolle. Angeln an anderen Plätzen.</li>
</ul>
<ul>
    <li><strong>Außerhalb der Saison: vom 08.01. bis 30.04. und vom 16.09. bis 15.12. (außer bei Veranstaltungen)</strong></li>
    <li>Erlaubt: Stippangeln (ohne Rolle) sowie Angeln mit Rolle an den gekennzeichneten Plätzen.</li>
</ul>

