<p>Erliefnis Baggerweier a.s.b.l. haftet nicht für Verletzungen, Bisse sowie Sach- oder Personenschäden, die während oder nach Ihrem Besuch des Hundebereichs am Baggerweier entstehen.</p>

<p>Außerhalb des Wassers müssen Hunde an der Leine geführt werden. Die Hundehalter sind verpflichtet, ihre Hunde unter Kontrolle zu halten.</p>

<p>Jeder Hund muss einen gültigen Impfpass sowie eine gültige Tollwutimpfung vorweisen.</p>

<p>Die Besitzer oder Betreuer von Hunden müssen verhindern, dass diese den Hundebereich und seine Umgebung durch ihre Exkremente verschmutzen.</p>

<p>Die Besitzer oder Halter von Hunden müssen die notwendigen Maßnahmen treffen, damit Hunde durch wiederholtes Bellen oder Heulen nicht die öffentliche Ruhe stören.</p>

<p>Ausschließlich die Besitzer oder Halter der Hunde sind für Vorfälle oder Unfälle verantwortlich, die durch ihre Hunde verursacht werden.</p>

<p>Hunde sind nur im Bereich des Hundestrands (Zone 6) sowie auf der Terrasse der Buvette (Zone 2) erlaubt.</p>

<p>Hunde müssen jederzeit an der Leine und unter der Kontrolle ihrer Halter bleiben.</p>

<h2>Keine Badeaufsicht</h2>
<p>Die Direktion lehnt jede Verantwortung bei Unfällen oder bei Nichtbeachtung der Sicherheitsanweisungen ab.</p>